# Задание №3
# Два инвестора - Майкл и Иван хотят вложиться в стартап. Фаундеры сказали, что минимальная сумма инвестиций - X долларов,
# больше инвестировать можно сколько угодно. У Майкла A долларов, у Ивана B долларов.
# Если оба могут вложиться - выведите 2, если только Майкл - Mike, если только Иван - Ivan,
# если не могут по отдельности, но вместе им хватает - 1, если никто - 0.

x = int(input("Минимальная сумма инвестиций X: "))
a = int(input("Сумма Майкла A: "))
b = int(input("Сумма Ивана B: "))

mike = a >= x
ivan = b >= x

if mike and ivan:
    print(2)
elif mike:
    print("Mike")
elif ivan:
    print("Ivan")
elif a + b >= x:
    print(1)
else:
    print(0)
    